#include <iostream>
using namespace std;

int main() {
    // declare a variable
    int w;
    // input the variable
    cin>>w;
    // check if the number is more than two AND is even
    if (w > 2 && w % 2 == 0) {
        // print yes
        cout<<"YES";
    }  else {
        // print no
        cout<<"NO";
    }

}
